#include "uvm.h"

t_ub	isa_jci(t_arg a)
{
	t_ub	x;
	t_uw	y;

	x = stk_pop(a.s, a.mk);
	if (!x)
		*a.pc += 2;
	if (!x)
		return (0);
	y = a.u->ram[(*a.pc)++] << 8;
	y = y | a.u->ram[(*a.pc)++];
	*a.pc += y;
	return (0);
}
