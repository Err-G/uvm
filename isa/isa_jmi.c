#include "uvm.h"

t_ub	isa_jmi(t_arg a)
{
	t_uw	x;

	x = a.u->ram[(*a.pc)++] << 8;
	x = x | a.u->ram[(*a.pc)++];
	*a.pc += x;
	return (0);
}
