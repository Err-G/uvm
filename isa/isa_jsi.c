#include "uvm.h"

t_ub	isa_jsi(t_arg a)
{
	t_uw	x;

	stk_push2(a.s, *a.pc + 2);
	x = a.u->ram[*a.pc++] << 8;
	x = x | a.u->ram[*a.pc++];
	*a.pc += x;
	return (0);
}
