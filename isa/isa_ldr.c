#include "uvm.h"

t_ub	isa_ldr(t_arg a)
{
	t_sb	x;

	x = stk_pop(a.s, a.mk);
	stk_pushx(a.s, aux_peek(a.u, *a.pc + x, a.m2), a.m2);
	return (0);
}
