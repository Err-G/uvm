#include "uvm.h"

t_ub	isa_nip(t_arg a)
{
	t_uw	x;

	x = stk_popx(a.s, a.mk, a.m2);
	stk_popx(a.s, a.mk, a.m2);
	stk_pushx(a.s, x, a.m2);
	return (0);
}
